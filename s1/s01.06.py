# Escribir un programa que solicite al usuario un numero de 1 al 5 e imprima el numero en letras.

n = int(input("N: "))

if n == 1:
    print("Uno")
elif n == 2:
    print("Dos")
elif n == 3:
    print("Tres")
elif n == 4:
    print("Cuatro")
elif n == 5:
    print("Cinco")
else:
    print("Otro")
